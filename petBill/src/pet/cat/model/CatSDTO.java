package pet.cat.model;

public class CatSDTO {

	private int subNo;
	private String subLptNo;
	private String subName;
	private String subImg;
	
	
	public int getSubNo() {
		return subNo;
	}
	public void setSubNo(int subNo) {
		this.subNo = subNo;
	}
	public String getSubLptNo() {
		return subLptNo;
	}
	public void setSubLptNo(String subLptNo) {
		this.subLptNo = subLptNo;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public String getSubImg() {
		return subImg;
	}
	public void setSubImg(String subImg) {
		this.subImg = subImg;
	}
	
}
